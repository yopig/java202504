package ch03.lecture;

public class C01Arithmetic {
    public static void main(String[] args) {


        int a = 3;
        int b = 5;
        System.out.println((a + b));

        int c = 7;
        int d = 2;
        System.out.println(c - d);

        int e = 9;
        int f = 3;
        System.out.println(e * f);

        int g = 10;
        int h = 3;
        System.out.println(g / h);

        // % 모듈로 연산 나머지 연산
        int i = 10;
        int j = 4;
        System.out.println(i % j);
    }
}
