package ch03.lecture;

public class C09Compare {
    public static void main(String[] args) {
        // 비교 연산자
        int a = 10;
        int b = 30;
        System.out.println(a > b);
        System.out.println(a < b);
        // >= <=
        System.out.println(a >= b);
        System.out.println(a <= b);

        // ==

        System.out.println(a == b);
        System.out.println(a != b);
    }
}
