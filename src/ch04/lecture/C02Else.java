package ch04.lecture;

public class C02Else {
    public static void main(String[] args) {
        //else : if 조건이 false일때 실행되는 ㅗ드블럭

        if (true) {
            System.out.println("코드!!");
        } else {
            System.out.println("코드@@");
        }
        if (false) {
            System.out.println("코드###");
        } else {
            System.out.println("코드$$");
        }

    }
}
