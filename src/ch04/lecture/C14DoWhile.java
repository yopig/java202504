package ch04.lecture;

public class C14DoWhile {
    public static void main(String[] args) {
        // while 조건 확인 후 실행

        System.out.println("code 1");
        boolean a = true;
        do {
            System.out.println("code 1");
            System.out.println("code 1");
        } while (a);
        System.out.println("code 1");
    }
}
