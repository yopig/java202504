package ch02.lecture;

public class C06realnumber {
    public static void main(String[] args) {
        // 실수
        // float   4byte
        float a = 3.14F;
        System.out.println(a);
        // double  8byte
        double b = 3.14;
        System.out.println(b);
        // 더 넓은 범위를 더 촘촘하게 표현함
        //
    }
}
