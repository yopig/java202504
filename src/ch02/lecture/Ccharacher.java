package ch02.lecture;

public class Ccharacher {
    public static void main(String[] args) {
        // char 문자열
        // 하나으 문자를 저장하는 자료형
        // 2bytes

        char a;
        a = 'a';
        System.out.println(a);
        a = 'b';
        System.out.println(a);
        a = 'c';
        System.out.println(a);
//        a = '한글';
        System.out.println(a);
        a = '\'';
        System.out.println(a);
        a = '\ud55c';
        System.out.println(a);

    }
}
